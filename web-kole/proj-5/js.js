// 1. prompt — қолданушыдан мәлімет сұрау
let name = prompt("Атыңызды енгізіңіз:");

// 2. confirm — растауды сұраймыз
let isSure = confirm("Сіз жалғастырғыңыз келетініне сенімдісіз бе?");

// 3. alert — нәтижені көрсету
if (name && isSure) {
    alert("Сәлем, " + name + "! Сіз әрекетті растадыңыз.");
} else if (name) {
    alert("Сәлем, " + name + "! Сіз әрекетті тоқтаттыңыз.");
} else {
    alert("Сіз ат енгізбедіңіз.");
}
